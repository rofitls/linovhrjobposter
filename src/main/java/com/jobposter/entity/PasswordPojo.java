package com.jobposter.entity;

public class PasswordPojo {

	private String id;
	private String oldPas;
	private String newPas;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getOldPas() {
		return oldPas;
	}
	public void setOldPas(String oldPas) {
		this.oldPas = oldPas;
	}
	public String getNewPas() {
		return newPas;
	}
	public void setNewPas(String newPas) {
		this.newPas = newPas;
	}
	
}
